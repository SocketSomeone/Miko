export enum Permissions {
	// GENERAL PERMISSIONS
	ADMINISTRATOR = 0x8,
	VIEW_AUDIT_LOG = 0x80,
	MANAGE_SERVER = 0x20,
	MANAGE_ROLES = 0x10000000,
	MANAGE_CHANNELS = 0x10,
	KICK_MEMBERS = 0x2,
	BAN_MEMBER = 0x4,
	CREATE_INVITE = 0x1,
	CHANGE_NICKNAMES = 0x4000000,
	MANAGE_NICKNAMES = 0x8000000,
	MANAGE_EMOJIS = 0x40000000,
	MANAGE_WEBHOOKS = 0x20000000,

	// TEXT PERMISSIONS
	READ_MESSAGES = 0x400,
	SEND_MESSAGE = 0x800,
	SEND_TTS = 0x1000,
	MANAGE_MESSAGES = 0x2000,
	EMBED_LINKS = 0x4000,
	ATTACH_FILES = 0x8000,
	READ_HISTORY = 0x10000,
	MENTION = 0x20000,
	EXTERNAL_EMOJIS = 0x40000,
	ADD_REACTIONS = 0x40,

	// VOICE PERMISSIONS
	VIEW_CHANNEL = 0x400,
	CONNECT = 0x100000,
	SPEAK = 0x200000,
	MUTE_MEMBERS = 0x400000,
	MOVE_MEMBERS = 0x1000000,
	DEAFEN_MEMBERS = 0x800000,
	VOICE_ACTIVITY = 0x2000000,
	PRIORITY_SPEAKER = 0x100
}

export default (...permissions: Permissions[]): number => {
	return permissions.reduce((acc, perm) => (acc += perm), 0);
};
